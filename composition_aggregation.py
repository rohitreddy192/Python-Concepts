# Aggregation (Has-A)
# -> It is a unidirectional Association i.e., one way relationship. Ex:- A dept can have students but viceversa cannot be possible
# -> In Aggregation both entities can survive individually as one entity wont effect the other one.

# Composition 
# -> It is a restricted form of aggregation where two entities are highly dependent on each other.
# -> When there is composition relationship, the composed object can't survive without the other entity.
# -> When Employee object is deleted his salary entity also gets deleted.